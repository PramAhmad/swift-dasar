// integer
var age: Int = 30
print(age)
// interger punya banyak tipe data, ada Int8, Int16, Int32, Int64
// Int8 itu bisa menyimpan angka dari -128 sampai 127
// Int16 itu bisa menyimpan angka dari -32768 sampai 32767
// Int32 itu bisa menyimpan angka dari -2147483648 sampai 214748364
//  Int64 itu bisa menyimpan angka dari -9223372036854775808 sampai 9223372036854775807
var smallNumber: Int8 = 127
print(smallNumber)
var mediumNumber: Int16 = 32767
print(mediumNumber)
var largeNumber: Int32 = 2147483647
print(largeNumber)
var veryLargeNumber: Int64 = 9223372036854775807
print(veryLargeNumber)

// UNSIGNED INTEGER
// unsigned integer itu hanya bisa menyimpan angka positif, ada UInt8, UInt16, UInt32, UInt64
// UInt8 itu bisa menyimpan angka dari 0 sampai 255
// UInt16 itu bisa menyimpan angka dari 0 sampai 65535
// UInt32 itu bisa menyimpan angka dari 0 sampai 4294967295
// UInt64 itu bisa menyimpan angka dari 0 sampai 18446744073709551615
var unsignedSmallNumber: UInt8 = 255
print(unsignedSmallNumber)
var unsignedMediumNumber: UInt16 = 65535
print(unsignedMediumNumber)
var unsignedLargeNumber: UInt32 = 4294967295
print(unsignedLargeNumber)
var unsignedVeryLargeNumber: UInt64 = 18446744073709551615
print(unsignedVeryLargeNumber)

// floating point
var pi: Float = 3.14
print(pi)
var e: Double = 2.71828
print(e)




